    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbnSP.getModel();
        dtm.setRowCount(0);
        for (ChiTietSPView object : this.serC.getAllNhanVien()) {
            dtm.addRow(new Object[]{
                object.getSp(),
                object.getMs(),
                object.getNsx(),
                object.getDsx(),
                object.getNamBH(),
                object.getGianhap(),
                object.getGiaBan(),
                object.getSoLuongTon(),
                object.getMota()
            });

        }
    }

    private boolean checkTenSp(String ten, boolean check) {
        for (int i = 0; i < this.serC.getAllNhanVien().size(); i++) {
            if (tbnSP.getSelectedRow() == i && check == false) {
            } else {
                if (this.serC.getAllNhanVien().get(i).getSp().getTen().equalsIgnoreCase(ten)) {
                    JOptionPane.showMessageDialog(this, "Sản phẩm đã có trong danh sách");
                    return false;
                }

            }
        }
        return true;
    }

    private long ranDomMA() {
        return new Random().nextLong(100000);
    }

    private ChiTietSPView getData() {
        if (this.jcheck.checkData(ltext, this) == false) {
            return null;
        }
        return new ChiTietSPView(Integer.parseInt(txtNBH.getText().trim()), txtMT.getText().trim(), Integer.parseInt(txtSL.getText().trim()),
                Double.parseDouble(txtGN.getText().trim()), Double.parseDouble(txtGB.getText().trim()),
                new SanPhamView(ranDomMA() + "", txtT.getText().trim()),
                new MauSacView(ranDomMA() + "", txtMS.getText().trim()),
                new DongSanXuatView(ranDomMA() + "", txtLSP.getText().trim()),
                new NhaSanXuatView(ranDomMA() + "", txtNSX.getText().trim()));
    }

