    private void tbHienThiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHienThiMouseClicked
        if (this.jcheck.checkClcick(tbHienThi, this) == false) {
            return;
        }
        this.jcheck.clickTable(jtext, tbHienThi);
        dccCV.setSelectedItem(tbHienThi.getValueAt(tbHienThi.getSelectedRow(), 5));
        dccCh.setSelectedItem(tbHienThi.getValueAt(tbHienThi.getSelectedRow(), 6));
        nvm = this.nhanVienSevices.timID(tbHienThi.getValueAt(tbHienThi.getSelectedRow(), 0).toString());
    }//GEN-LAST:event_tbHienThiMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (this.jcheck.checkData(jtext, this) == false) {
            return;
        }
        NhanVienView nvv = getNVV();

        if (this.nhanVienSevices.addNV(nvv) == 0) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
            return;
        }
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        loadTable();
        this.jcheck.clearView(jtext, tbHienThi);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (this.jcheck.checkData(jtext, this) == false) {
            return;
        }
        NhanVienView nvv = getNVV();

        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không ?") == 0) {
            if (this.nhanVienSevices.updateNhanVien(nvv, nvm) == 0) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                return;
            }
        } else {
            return;
        }
        JOptionPane.showMessageDialog(this, "Cập nhập thành công");
        loadTable();
        this.jcheck.clearView(jtext, tbHienThi);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (this.jcheck.checkData(jtext, this) == false) {
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?") == 0) {
            if (this.nhanVienSevices.deleteNhanVien(nvm) == 0) {
                JOptionPane.showMessageDialog(this, "Xóa thật bại");
                return;
            }
        } else {
            return;
        }
        loadTable();
        this.jcheck.clearView(jtext, tbHienThi);
    }//GEN-LAST:event_btnXoaActionPerformed

