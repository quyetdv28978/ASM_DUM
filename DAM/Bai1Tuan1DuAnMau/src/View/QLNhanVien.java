    private void loadComCuaHang() {
        for (CuaHangView object : this.serch.getAllNhanVien()) {
            dccCh.addElement(object);
        }
        cbbCH.setModel((DefaultComboBoxModel) dccCh);
    }

    private void loadComChucVu() {
        for (ChucVuVIew object : this.sercv.getAllNhanVien()) {
            dccCV.addElement(object);
        }
        cbbCV.setModel((DefaultComboBoxModel) dccCV);
    }

    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbHienThi.getModel();
        dtm.setRowCount(0);
        for (NhanVienView n : this.nhanVienSevices.getAllNhanVien()) {
            dtm.addRow(new Object[]{
                n.getMa(),
                n.getTen(),
                n.getGioitinh() == true ? "Nam" : "Nữ",
                new SimpleDateFormat("yyyy-MM-dd").format(n.getNsDate()),
                n.getDiachi(),
                n.getTenChucVu(),
                n.getTenDiaChi()
            });
        }
    }

    private NhanVienView getNVV() {
        Date d = null;
        try {
            d = new Date(new SimpleDateFormat("yyyy-MM-dd").parse(txtNS.getText().trim()).getTime());
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        return new NhanVienView(jcheck.randomMA(), txtTen.getText().trim(),
                rdNam.isSelected() == true ? true : false,
                txtDia.getText(),
                (ChucVuVIew) dccCV.getSelectedItem(),
                (CuaHangView) dccCh.getSelectedItem(),
                 d);
    }

