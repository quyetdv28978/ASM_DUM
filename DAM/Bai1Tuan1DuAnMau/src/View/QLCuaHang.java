    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbnQLCH.getModel();
        dtm.setRowCount(0);
        for (CuaHangView object : chSer.getAllNhanVien()) {
            dtm.addRow(new Object[]{
                object.getMa(),
                object.getTen(),
                object.getDiachi()
            });
        }
    }

