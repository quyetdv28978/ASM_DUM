    private void tbnQLCHMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbnQLCHMouseClicked
        if (this.jcheck.checkClcick(tbnQLCH, this) == false) {
            return;
        }
        chm = this.chSer.timID(tbnQLCH.getValueAt(tbnQLCH.getSelectedRow(), 0).toString());
        this.jcheck.clickTable(listText, tbnQLCH);
    }//GEN-LAST:event_tbnQLCHMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (this.jcheck.checkData(listText, this) == false) {
            return;
        }
        CuaHangView ch = new CuaHangView(txtM.getText().trim(), txtTEN.getText().trim(), txtDC.getText().trim());
        if (this.chSer.addNV(ch) == 0) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
            return;
        }
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        loadTable();
        this.jcheck.clearView(listText, tbnQLCH);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (this.jcheck.checkClcick(tbnQLCH, this) == false) {
            return;
        }
        if (jcheck.checkData(listText, this) == false) {
            return;
        }
        CuaHangView cv = new CuaHangView(txtM.getText().trim(), txtTEN.getText().trim(), txtDC.getText().trim());
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?") != 0) {
            return;
        }
        if (this.chSer.updateNhanVien(cv, chm) == 0) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
            return;
        }
        loadTable();
        this.jcheck.clearView(listText, tbnQLCH);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (this.jcheck.checkClcick(tbnQLCH, this) == false) {
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?") == 0) {
            if (this.chSer.deleteNhanVien(chm) == 0) {
                JOptionPane.showMessageDialog(this, "Xóa thật bại");
                return;
            }
        } else {
            return;
        }
        loadTable();
        this.jcheck.clearView(listText, tbnQLCH);
    }//GEN-LAST:event_btnXoaActionPerformed

