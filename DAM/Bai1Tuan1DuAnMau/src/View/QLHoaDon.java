    private void loadTable() {
        String trangthaiString = null;
        DefaultTableModel dtm = (DefaultTableModel) tbnHoaDOn.getModel();
        dtm.setRowCount(0);
        for (HoaDonView object : this.serH.getHoaDon()) {
            if (object.getTrangThai() == 0) {
                trangthaiString = "Đang chờ xử lí";
            } else if (object.getTrangThai() == 1) {
                trangthaiString = "Đã hoàn thành";
            } else {
                trangthaiString = "Đã hủy";
            }
            dtm.addRow(new Object[]{
                object.getMa(),
                object.getNgayTao(),
                object.getNgayThanhToan(),
                trangthaiString,
                object.getKh().getTen(),
                object.getKh().getDiachi(),
                object.getKh().getSdt()
            });
        }
    }

