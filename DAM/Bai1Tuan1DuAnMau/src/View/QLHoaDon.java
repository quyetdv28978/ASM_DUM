    private void tbnHoaDOnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbnHoaDOnMouseClicked
        khm = this.khachHangSer.findNV(this.serH.getHoaDon().get(tbnHoaDOn.getSelectedRow()).getKh().getMa()).getMa();
        this.serH.getIDKhachhang(khm);
        this.jcheck.clickTable(jtext, tbnHoaDOn);
        double gia1 = 0;
        int sl1 = 0;
        listSp.removeAllElements();
        idhd = this.serH.getHoaDon().get(tbnHoaDOn.getSelectedRow()).getId();
        for (ChiTietHoaDonView chiTietSanPham : this.serH.getAllGioHangCT(idhd)) {
            sl1 += chiTietSanPham.getSl();
            listSp.addElement(chiTietSanPham.getCt().getSp().getTen() + " " + chiTietSanPham.getSl());
            gia1 += chiTietSanPham.getDonGia();

        }
        txtsl.setText(sl1 + "");
        txtDG.setText(gia1 + "");
        if (tbnHoaDOn.getValueAt(tbnHoaDOn.getSelectedRow(), 3).toString().equalsIgnoreCase("Đang chờ xử lí")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        nv.setSelectedItem(this.serH.getHoaDon().get(tbnHoaDOn.getSelectedRow()).getNv());
    }//GEN-LAST:event_tbnHoaDOnMouseClicked

