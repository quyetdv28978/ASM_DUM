    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (this.jcheck.checkData(ltext, this) == false) {
            return;
        }
        if (this.serC.addNV(new ChucVuVIew(txtMa.getText().trim(), txtTen.getText().trim())) == 0) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
            return;
        }

        JOptionPane.showMessageDialog(this, "Thêm thành công");
        loadTable();
        this.jcheck.clearView(ltext, tbnCV);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (this.jcheck.checkClcick(tbnCV, this) == false) {
            return;
        }
        if (jcheck.checkData(ltext, this) == false) {
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?") != 0) {
            return;
        }
        if (this.serC.updateNhanVien(new ChucVuVIew(txtMa.getText().trim(), txtTen.getText().trim()), cvm) == 0) {
            JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
            return;
        }
        loadTable();
        this.jcheck.clearView(ltext, tbnCV);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (this.jcheck.checkClcick(tbnCV, this) == false) {
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?") == 0) {
            if (this.serC.deleteNhanVien(cvm) == 0) {
                JOptionPane.showMessageDialog(this, "Xóa thật bại");
                return;
            }
        } else {
            return;
        }
        loadTable();
        this.jcheck.clearView(ltext, tbnCV);
    }//GEN-LAST:event_btnXoaActionPerformed

