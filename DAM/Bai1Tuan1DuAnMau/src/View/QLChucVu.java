    private void tbnCVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbnCVMouseClicked
        this.jcheck.checkClcick(tbnCV, this);
        this.jcheck.clickTable(ltext, tbnCV);
        cvm = this.serC.timID(tbnCV.getValueAt(tbnCV.getSelectedRow(), 0).toString());
    }//GEN-LAST:event_tbnCVMouseClicked

