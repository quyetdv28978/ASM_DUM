    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) tbnCV.getModel();
        dtm.setRowCount(0);
        for (Object thi : this.serC.getAllNhanVien()) {
            dtm.addRow(new Object[]{
                ((ChucVuVIew) thi).getMa(),
                ((ChucVuVIew) thi).getTen()
            });
        }
    }

