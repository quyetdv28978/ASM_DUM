    private void CbbDonHangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CbbDonHangItemStateChanged
        int sl1 = 0;
        double gia = 0;
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            listSanphamview.removeAllElements();
            loadList();
            for (ChiTietHoaDonView chiTietHoaDonView : this.ser.getAllCTSanPham("where g.id = " + "'" + ((HoaDonView) dccHD.getSelectedItem()).getId() + "'")) {
                txtD.setText(chiTietHoaDonView.getHd().getDiaChi());
                sl1 += chiTietHoaDonView.getSl();
                gia += chiTietHoaDonView.getDonGia();
                txtGia.setText(String.valueOf(gia));
                txtS.setText(String.valueOf(sl1));
                System.out.println("ngay nhan: " + chiTietHoaDonView.getHd().getNgayNhan());
                if (chiTietHoaDonView.getHd().getNgayNhan() != null) {
                    DateNhan.setText(new SimpleDateFormat("yyyy/MM/dd").format(chiTietHoaDonView.getHd().getNgayNhan()));
                } else {
                    DateNhan.setText("");
                }

                if (chiTietHoaDonView.getHd().getTrangThai() == 0) {
                    txtTT.setText("Đang xử lý");
                } else if (chiTietHoaDonView.getHd().getTrangThai() == 1) {
                    txtTT.setText("Đã hoàn thành");
                } else {
                    txtTT.setText("Đã hủy");
                }
            }
        }
    }//GEN-LAST:event_CbbDonHangItemStateChanged

