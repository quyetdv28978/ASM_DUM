    private void loadList() {
        for (ChiTietHoaDonView chiTietHoaDonView : this.ser.getAllCTSanPham("where g.id = " + "'" + ((HoaDonView) dccHD.getSelectedItem()).getId() + "'")) {
            listSanphamview.addElement(chiTietHoaDonView.getCt().getSp().getTen() + " " + chiTietHoaDonView.getSl());
        }
        Lsp.setModel((DefaultListModel) listSanphamview);
    }

    private double gia(String ma) {
        double gia = 0;
        double g = this.ser.getCT(ma).getGiaBan();
        String ten = this.ser.getCT(ma).getSp().getTen();
        for (int i = 0; i < lGioHang.getSize(); i++) {
            if (lGioHang.get(i).getTen().equalsIgnoreCase(ten)) {
                gia += g;
            }
        }
        return gia;
    }

    private String epDate(Date d) {
        return new SimpleDateFormat("yyyy/MM/dd").format(d);
    }

    private int checkSL(String ma) {
        int sl1 = 0;

        for (int j = 0; j < lGioHang.getSize(); j++) {
            if (lGioHang.get(j).getTen().equalsIgnoreCase(ma)) {

                sl1++;
            }
        }
        if (sl1 > sl) {
            JOptionPane.showMessageDialog(this, "Đã hết hàng");
            txtSL.setText("0");
            lGioHang.remove(lGioHang.size() - 1);
            return 28978;
        }
        txtSL.setText(sl - sl1 + "");
        return sl1++;
    }

