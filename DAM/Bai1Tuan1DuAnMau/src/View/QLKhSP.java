    private void cbbSPItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbSPItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (((SanPhamView) dccSP.getSelectedItem()).getTen() != null) {
                ct.add(((SanPhamView) dccSP.getSelectedItem()).getMa());
                for (String string : ct) {
                    if (string.equalsIgnoreCase(((SanPhamView) dccSP.getSelectedItem()).getMa())) {
                        sl = ser.getCT(string).getSoLuongTon();
                        break;
                    }
                }
                txtSL.setText(sl + "");
                System.out.println(txtSL.getText().trim());
                if (Integer.parseInt(txtSL.getText().trim()) < 0) {
                    return;
                }
                lGioHang.addElement((SanPhamView) dccSP.getSelectedItem());
                checkSL(((SanPhamView) dccSP.getSelectedItem()).getTen());
            }
        }
    }//GEN-LAST:event_cbbSPItemStateChanged

