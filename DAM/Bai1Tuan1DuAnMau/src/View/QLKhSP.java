    private void btnTTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTTActionPerformed
        String ma;
        ChiTietSPView chiTietSPView;
        for (String string : ct) {
            this.ser.getTenSP(this.ser.getCT(string).getSp().getTen());
            ma = this.ser.getCT(string).getSp().getTen();
            if (checkSL(ma) == 0) {
                return;
            }
            chiTietSPView = this.serCTSanPham.findNV(ma);
            chiTietSPView.setSoLuongTon(chiTietSPView.getSoLuongTon() - checkSL(ma));
            ChiTietGioHangView ct1 = new ChiTietGioHangView(checkSL(ma),
                    gia(string), 0, chiTietSPView);
            if (this.ser.addNV(ct1) == 0) {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
                return;
            }
        }
        this.ser.addHoaDon();
        this.ser.addCTHD();
        txtSL.setText("");
        dccSP.setSelectedItem(new SanPhamView());
        cbbKH.setSelectedIndex(0);
        lGioHang.removeAllElements();
    }//GEN-LAST:event_btnTTActionPerformed

